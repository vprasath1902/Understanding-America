(function () {
  const body = document.body;
  const sidebar = document.querySelector('.sidebar');
  const search = document.querySelector('#search');
  const progress = document.querySelector('.progress');

  function relHref(href) {
    const inSubfolder = location.pathname.includes('/chapters/') || location.pathname.includes('/appendices/');
    return inSubfolder ? '../' + href : href;
  }

  function currentHref() {
    const parts = location.pathname.split('/');
    const file = parts[parts.length - 1] || 'index.html';
    const folder = parts[parts.length - 2];
    if (folder === 'chapters') return 'chapters/' + file;
    if (folder === 'appendices') return 'appendices/' + file;
    return 'index.html';
  }

  function buildNavigation() {
    const navLists = document.querySelectorAll('.nav-list');
    navLists.forEach(list => {
      list.innerHTML = '';
      const home = document.createElement('li');
      home.innerHTML = `<a href="${relHref('index.html')}" class="${currentHref()==='index.html'?'active':''}">Home / Cover</a>`;
      list.appendChild(home);
      let lastSection = '';
      BOOK_NAV.forEach(item => {
        if (item.section !== lastSection) {
          const label = document.createElement('li');
          label.className = 'part-label';
          label.textContent = item.section;
          list.appendChild(label);
          lastSection = item.section;
        }
        const li = document.createElement('li');
        const active = currentHref() === item.href ? ' active' : '';
        li.innerHTML = `<a class="${active}" href="${relHref(item.href)}"><span>${item.title}</span></a>`;
        list.appendChild(li);
      });
    });
  }

  function filterNavigation(query) {
    const q = query.toLowerCase().trim();
    document.querySelectorAll('.nav-list li').forEach(li => {
      if (li.classList.contains('part-label')) {
        li.style.display = q ? 'none' : '';
        return;
      }
      const text = li.textContent.toLowerCase();
      li.style.display = !q || text.includes(q) ? '' : 'none';
    });
  }

  window.toggleMenu = function () { sidebar && sidebar.classList.toggle('open'); };
  window.openReaderPanel = function () { const p = document.getElementById('readerPanel'); if (p) p.classList.toggle('open'); };
  window.setTheme = function (theme) { body.classList.remove('dark','sepia'); if (theme === 'dark') body.classList.add('dark'); if (theme === 'sepia') body.classList.add('sepia'); localStorage.setItem('ua-theme', theme); };
  window.toggleTheme = function () { setTheme(body.classList.contains('dark') ? 'light' : 'dark'); };
  window.toggleLargeText = function () { body.classList.toggle('large-text'); localStorage.setItem('ua-large-text', body.classList.contains('large-text') ? 'yes' : 'no'); };
  window.toggleReaderMode = function () { body.classList.toggle('reader-mode'); localStorage.setItem('ua-reader-mode', body.classList.contains('reader-mode') ? 'yes' : 'no'); };
  window.printBook = function () { window.print(); };

  const savedTheme = localStorage.getItem('ua-theme');
  if (savedTheme === 'dark') body.classList.add('dark');
  if (savedTheme === 'sepia') body.classList.add('sepia');
  if (localStorage.getItem('ua-large-text') === 'yes') body.classList.add('large-text');
  if (localStorage.getItem('ua-reader-mode') === 'yes') body.classList.add('reader-mode');
  buildNavigation();

  document.querySelectorAll('#search').forEach(input => {
    input.addEventListener('input', () => filterNavigation(input.value));
  });

  window.addEventListener('scroll', function () {
    const h = document.documentElement;
    const denom = h.scrollHeight - h.clientHeight;
    const pct = denom > 0 ? (h.scrollTop / denom) * 100 : 0;
    if (progress) progress.style.width = Math.max(0, Math.min(100, pct)) + '%';
  });
})();


